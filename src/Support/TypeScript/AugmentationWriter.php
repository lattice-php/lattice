<?php

declare(strict_types=1);

namespace Lattice\Lattice\Support\TypeScript;

use Illuminate\Support\Facades\File;

final class AugmentationWriter
{
    public function __construct(private readonly PropsTypeGenerator $propsTypeGenerator) {}

    /**
     * @param  list<DiscoveredComponent>  $discovered
     */
    public function write(array $discovered, string $output, string $module): void
    {
        $entries = array_filter(
            $discovered,
            static fn (DiscoveredComponent $c): bool => $c->category === 'field' || $c->category === 'component',
        );

        usort(
            $entries,
            fn (DiscoveredComponent $a, DiscoveredComponent $b): int => $a->type <=> $b->type,
        );

        $lines = [];

        foreach ($entries as $component) {
            $propsType = $this->propsTypeGenerator->forClass($component->class);
            $indented = $this->indent($propsType, 4);
            $lines[] = sprintf('    "%s": %s;', $component->type, $indented);
        }

        $body = $lines === [] ? '' : "\n".implode("\n", $lines)."\n  ";

        $content = implode("\n", [
            '// This file is generated by `php artisan lattice:typescript`. Do not edit.',
            sprintf('declare module "%s" {', $module),
            '  interface LatticeComponentProps {',
            '  '.$body,
            '  }',
            '}',
            '',
            'export {};',
            '',
        ]);

        File::ensureDirectoryExists(dirname($output));
        File::put($output, $content);
    }

    /** Indent every line except the first by $spaces, preserving blank lines. */
    private function indent(string $type, int $spaces): string
    {
        if (! str_contains($type, "\n")) {
            return $type;
        }

        $pad = str_repeat(' ', $spaces);
        $lines = explode("\n", $type);

        $indented = array_map(
            static fn (string $line, int $i): string => ($i === 0 || trim($line) === '') ? $line : $pad.$line,
            $lines,
            array_keys($lines),
        );

        return implode("\n", $indented);
    }
}
